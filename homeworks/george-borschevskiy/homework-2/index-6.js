// Перевірка на високосність року. Умови:
// 1) Якщо рік не ділиться на 4, то він звичайний.
// 2) Інакше треба перевірити, чи ділиться рік на 100 і не ділиться на 400, якщо так, то звичайний.
// 3) Інакше високосний

const year = +prompt('enter a year')

if (year % 4 == 0 && (year % 100 != 0 || year % 400 == 0)) {
    alert('високосний')
} else {
    alert('не високосний')
}